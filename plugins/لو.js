let handler = async (m, { conn, args, usedPrefix, command }) => {
  const vipStatus = global.tierBadge ? global.tierBadge(m.sender) : (isVip(m.sender) ? '💎 مميز' : '👤 عادي')

  const sub = (args[0] || '').trim().toLowerCase()

  // عرض الفئات
  if (sub === 'فئات' || sub === 'categories') {
    const catList = CATEGORIES.map((c, i) => `${i + 1}. ${c.name} (${c.list.length} موقف)`).join('\n')
    return m.reply(
`🎭 *فئات لعبة "لو"*

${catList}

💡 لتحديد الفئة:
*.لو جامعي* / *.لو مهني* / *.لو تقني*
*.لو اجتماعي* / *.لو فلسفي* / *.لو مالي*
أو *.لو* فقط للعشوائي`)
  }

  let question = null
  let category = ''

  if (sub === 'جامعي') { question = pick(UNIVERSITY); category = '🎓 جامعي' }
  else if (sub === 'مهني' || sub === 'عمل') { question = pick(CAREER); category = '💼 مهني' }
  else if (sub === 'تقني' || sub === 'تكنولوجيا') { question = pick(TECH); category = '💻 تقني' }
  else if (sub === 'اجتماعي' || sub === 'مجتمع') { question = pick(SOCIAL); category = '👥 اجتماعي' }
  else if (sub === 'فلسفي' || sub === 'فلسفة') { question = pick(PHILOSOPHY); category = '🤔 فلسفي' }
  else if (sub === 'مالي' || sub === 'مال') { question = pick(FINANCE); category = '💵 مالي' }
  else {
    const cat = CATEGORIES[Math.floor(Math.random() * CATEGORIES.length)]
    question = pick(cat.list)
    category = cat.name
  }

  return m.reply(
`╭────『 🎭 لعبة لو 』────
│ ${category}
│
│ "${question}"
│
╰──────────────────

💬 _شارك رأيك بحرية!_
👤 ${vipStatus}`.trim())
}

handler.help = ['لو', 'لو جامعي', 'لو مهني', 'لو تقني']
handler.tags = ['fun', 'game']
handler.command = /^لو(\s.*)?$/i
export default handler
