function normalize(s) {
  return String(s || '').trim().toLowerCase()
    .replace(/[ًٌٍَُِّْـ]/g, '')
    .replace(/[أإآا]/g, 'ا')
    .replace(/ة/g, 'ه')
    .replace(/\s+/g, ' ').trim()
}

let handler = async (m, { conn, command, usedPrefix }) => {
  const vipStatus = global.tierBadge ? global.tierBadge(m.sender) : (isVip(m.sender) ? '💎 مميز' : '👤 عادي')
  conn.riddle = conn.riddle || {}
  const chatId = m.chat

  if (conn.riddle[chatId]) {
    const cur = conn.riddle[chatId]
    return conn.reply(m.chat,
      `⚠️ هناك لغز قائم لم يُحل بعد!\n\n🔮 *${cur.riddle.q}*\n\n💡 تلميح: _${cur.riddle.hint}_\n\nاكتب إجابتك مباشرة أو رُدّ على الرسالة`,
      cur.msg)
  }

  const picked = RIDDLES[Math.floor(Math.random() * RIDDLES.length)]
  const sent = await conn.reply(m.chat,
`╭────『 🔮 لغز جامعي 』────
│
│ ${picked.q}
│
│ 💡 تلميح: _${picked.hint}_
│ ⏱️ الوقت: ${TIMEOUT / 1000} ثانية
│ 💰 الجائزة: ${fmt(REWARD)}
│ ⭐ XP: +${XP_BONUS}
│
│ 💬 اكتب إجابتك مباشرة
╰──────────────────`.trim(), m)

  conn.riddle[chatId] = {
    riddle: picked,
    msgId: sent?.key?.id,
    msg: sent,
    timer: setTimeout(async () => {
      if (conn.riddle[chatId]) {
        await conn.reply(m.chat,
`⏰ *انتهى الوقت!*

✅ الإجابة كانت: *${picked.a}*

استخدم *${usedPrefix}فزورة* لتجربة لغز جديد`, conn.riddle[chatId].msg)
        delete conn.riddle[chatId]
      }
    }, TIMEOUT)
  }
}

handler.all = async function (m) {
  if (!this.riddle) return
  const chatId = m.chat
  const entry = this.riddle[chatId]
  if (!entry) return
  if (m.isBaileys) return

  const rawText = (m.text || '').trim()
  if (!rawText || global.prefix?.test?.(rawText)) return

  const correct = normalize(entry.riddle.a)
  const given   = normalize(rawText)

  if (!given || given.length < 1) return

  if (given.includes(correct) || correct.includes(given) && given.length >= 2) {
    clearTimeout(entry.timer)
    delete this.riddle[chatId]

    const user = global.db.data.users[m.sender] ||= {}
    initEconomy(user, m.sender)
    user.money = (user.money || 0) + REWARD
    user.exp   = (user.exp   || 0) + XP_BONUS
    user.totalEarned = (user.totalEarned || 0) + REWARD
    logTransaction(user, 'earn', REWARD, '🔮 حل لغز')
    await global.db.write()

    return this.reply(m.chat,
`╭────『 ✅ إجابة صحيحة! 』────
│
│ 🎉 أحسنت @${m.sender.split('@')[0]}!
│ ✅ الإجابة: *${entry.riddle.a}*
│
│ 💰 +${fmt(REWARD)}
│ ⭐ +${XP_BONUS} XP
│ 💼 رصيدك: ${fmt(user.money)}
╰──────────────────`.trim(), m, { mentions: [m.sender] })
  }

  // خاطئة — لا نرد إلا إذا كان reply مباشر
  const isDirect = m.quoted && entry.msgId && m.quoted.id === entry.msgId
  if (isDirect) {
    await this.reply(m.chat, `❌ إجابة خاطئة!\n💡 تلميح: _${entry.riddle.hint}_`, m)
  }
}

handler.help = ['فزورة', 'لغز']
handler.tags = ['game']
handler.command = /^(فزوره|فزورة|لغز|riddle)$/i
export default handler
