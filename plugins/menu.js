handler.all = async function (m) {
  const conn = this
  const session = global.menuSessions?.[m.sender]
  if (!session) return

  const prefix = session.prefix || '.'
  let choice = ''

  // Check if it's a list response
  if (m.mtype === 'listResponseMessage') {
    choice = m.msg.singleSelectReply?.selectedRowId || ''
  } else {
    // Check if it's a normal text message with prefix
    const raw = (m.text || '').trim()
    if (!raw.startsWith(prefix)) return
    const afterPrefix = raw.slice(prefix.length).trim()
    choice = normalizeChoice(afterPrefix)
  }

  const section = menuSections[choice]
  if (!section) return

  if (Date.now() - session.ts > 5 * 60 * 1000) {
    delete global.menuSessions[m.sender]
    return
  }

  // Don't hijack input if the sender is in an active game
  const inGame = conn.game && Object.values(conn.game).some(r =>
    r.state === 'PLAYING' && r.game &&
    [r.game.playerX, r.game.playerO].includes(m.sender)
  )
  if (inGame) return

  const user = global.db.data.users[m.sender] || {}
  initEconomy(user)
  syncEnergy(user, m.sender)

  const level = user.level || 0
  const role = getRole(level)
  const { max } = xpRange(level, global.multiplier)
  const uptime = clockString(process.uptime() * 1000)
  const stats = buildStats(m, user, level, role, max, uptime)

  await typingDelay(conn, m.chat, 1500)

  await conn.sendMessage(
    m.chat,
    { text: buildSection(prefix, section.key, stats) },
    { quoted: m }
  )

  delete global.menuSessions[m.sender]
}

