const menuSections = Object.fromEntries(
  Object.keys(sections).map((key, i) => [String(i + 1), { key, ...sections[key] }])
)

function buildStats(m, user, level, role, max, uptime) {
  const name = m.pushName || 'مستخدم'
  const money = user.money || 0
  const bank = user.bank || 0
  const energy = typeof user.energy === 'number' ? user.energy : 100
  const epct = Math.max(0, Math.min(10, Math.floor((energy / 100) * 10)))
  const ebar = '█'.repeat(epct) + '░'.repeat(10 - epct)
  const vip = user.premium || (user.premiumTime > Date.now())

  return `╔══〘 🌟 *SHADOW - Bot* 🌟 〙══╗
║
║  👤 *${name}*
║  🏆 المستوى: *${level}* │ ${role}
║  ⭐ XP: *${user.exp || 0} / ${max}*
║
║  ─── الأموال ───
║  💰 محفظة: *${money.toLocaleString('en')} 🪙*
║  🏦 بنك:   *${bank.toLocaleString('en')} 🪙*
║  💎 ماس:   *${user.diamond || 0}*
║  👑 عضوية: *${vip ? '💎 مميز (طاقة ∞)' : '⚡ عادي'}*
║
║  ─── الطاقة ───
║  ${ebar} ${vip ? '∞' : energy}/100 ⚡
║
║  ⏱️ وقت التشغيل: *${uptime}*
║
╚══〘 👇 اختر قسماً من القائمة 〙══╝`.trim()
}

function buildMenuText(prefix, stats) {
  const pageText = Object.entries(menuSections)
    .map(([num, s]) => `*${num}.* ${s.title}`)
    .join('\n')

  return `${stats}\n\n${pageText}\n\n💡 أرسل رقم القسم أو اختر من القائمة للتفاصيل`
}

function buildSection(prefix, key, stats) {
  return `${stats}\n\n${sections[key].text(prefix)}`
}

let handler = async (m, { conn, usedPrefix }) => {
  const user = global.db.data.users[m.sender] || {}
  initEconomy(user)
  syncEnergy(user, m.sender)

  const level = user.level || 0
  const role = getRole(level)
  const { max } = xpRange(level, global.multiplier)
  const uptime = clockString(process.uptime() * 1000)
  const stats = buildStats(m, user, level, role, max, uptime)

  await typingDelay(conn, m.chat, 2200)

  const rows = Object.entries(menuSections).map(([num, s]) => ({
    title: s.title,
    rowId: num,
    description: `عرض أوامر قسم ${s.title}`
  }))

  const button = {
    title: "📋 أقسام البوت",
    description: stats,
    footerText: "اختر قسماً من القائمة أدناه",
    buttonText: "📋 عرض الأقسام"
  }

  global.menuSessions ??= {}
  global.menuSessions[m.sender] = { prefix: usedPrefix, ts: Date.now() }

  try {
    // Attempt to send list message
    await conn.sendListM(m.chat, button, rows, m)
  } catch (e) {
    console.error("Error sending list message:", e)
    // Fallback to text message if list fails
    await conn.sendMessage(
      m.chat,
      { image: global.imagen4, caption: buildMenuText(usedPrefix, stats) },
      { quoted: m }
    )
  }
}

